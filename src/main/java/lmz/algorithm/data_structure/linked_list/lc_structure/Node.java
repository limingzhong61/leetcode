package lmz.algorithm.data_structure.linked_list.lc_structure;

// Definition for a Node.
public class Node {
    public int val;
    public Node next;

    public Node() {
    }

    public Node(int _val) {
        val = _val;
    }

    public Node(int _val, Node _next) {
        val = _val;
        next = _next;
    }
};
