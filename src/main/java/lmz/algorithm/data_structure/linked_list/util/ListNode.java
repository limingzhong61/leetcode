package lmz.algorithm.data_structure.linked_list.util;

/**
 * leetcode:
 * Definition for singly-linked list.
 */

public class ListNode {
    public int val;
    public ListNode next;

    public ListNode() {
    }

    public ListNode(int val) {
        this.val = val;
    }

    public ListNode(int val, ListNode next) {
        this.val = val;
        this.next = next;
    }
}
